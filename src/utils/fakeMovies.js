export const getFakeMovies = (searchTerm = '', limit = fakeMovies.length) => {
  const query = searchTerm.trim().toLowerCase();
  const matches = query
    ? fakeMovies.filter((movie) =>
        [movie.Title, movie.Genre, movie.Year].some((value) =>
          value.toLowerCase().includes(query)
        )
      )
    : fakeMovies;

  return (matches.length ? matches : fakeMovies).slice(0, limit);
};

export const getFakeMovieDetails = (imdbID) =>
  fakeMovies.find((movie) => movie.imdbID === imdbID);
